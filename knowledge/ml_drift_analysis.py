    ax.set_xlabel("Origin Latitude (°S)")
    ax.set_ylabel("Reunion Timed Hit Rate (%)")
    ax.set_title("Reunion Timing vs Latitude")
    ax.axvspan(-37, -34, alpha=0.1, color="green", label="Our zone")
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)

    # 2. Reunion timed hit rate vs longitude
    ax = axes[0, 1]
    ax.scatter(df.lon, df.timed_Reunion * 100, c="steelblue", s=20, alpha=0.7)

def exploratory_analysis(origin_idx, timed_hit):
    """Per-origin hit rates, multi-site scores, zone comparison."""
    n_origins = len(ORIGINS)
    n_sites = len(SITE_NAMES)

    # Compute per-origin timed hit rates
    rates = np.zeros((n_origins, n_sites))
    counts = np.zeros(n_origins, dtype=int)
    for oidx in range(n_origins):
        mask = origin_idx == oidx
        counts[oidx] = mask.sum()
        if counts[oidx] > 0:
            rates[oidx] = timed_hit[mask].mean(axis=0)

    # Multi-site weighted score
    weights = np.array([MULTI_SITE_WEIGHTS[name] for name in SITE_NAMES])
    multi_score = rates @ weights

    # Build DataFrame
    df = pd.DataFrame({
        "origin_idx": range(n_origins),
        "lat": [ORIGINS[i][0] for i in range(n_origins)],
        "lon": [ORIGINS[i][1] for i in range(n_origins)],
        "particles": counts,
    })
    for i, name in enumerate(SITE_NAMES):
        df[f"timed_{name}"] = rates[:, i]
    df["multi_score"] = multi_score

    # --- Print summary ---
    print("\n" + "=" * 80)
    print("TASK 1: EXPLORATORY ANALYSIS")
    print("=" * 80)

    # Top 10 by multi-site score
    top10 = df.nlargest(10, "multi_score")
    print("\nTop 10 origins by multi-site score:")
    print(f"{'Idx':>4} {'Lat':>8} {'Lon':>8} {'Reunion':>8} {'Moz_flap':>9} {'Tanz':>8} {'Multi':>8}")
    print("-" * 60)
    for _, row in top10.iterrows():
        print(f"{int(row.origin_idx):>4} {row.lat:>8.1f} {row.lon:>8.1f} "
              f"{row.timed_Reunion:>7.1%} {row.timed_Mozambique_flap:>8.1%} "
              f"{row.timed_Tanzania_Pemba:>7.1%} {row.multi_score:>8.3f}")

    # Zone comparison
    our_zone = df[(df.lon >= 90) & (df.lon <= 92) & (df.lat >= -37) & (df.lat <= -34)]
    atsb_zone = df[(df.lon >= 93) & (df.lon <= 98)]

    print("\n--- Zone comparison ---")
    for label, zone in [("Our zone (90-92E, 34-37S)", our_zone),
                        ("ATSB corridor (93-98E)", atsb_zone)]:
        if len(zone) == 0:
            print(f"  {label}: no origins in range")
            continue
        print(f"  {label}: {len(zone)} origins")
        print(f"    Mean Reunion timed rate:  {zone.timed_Reunion.mean():.1%}")
        print(f"    Mean multi-site score:    {zone.multi_score.mean():.3f}")
        print(f"    Best multi-site score:    {zone.multi_score.max():.3f}")

    if len(our_zone) > 0 and len(atsb_zone) > 0:
        ratio = our_zone.timed_Reunion.mean() / max(atsb_zone.timed_Reunion.mean(), 1e-9)
        print(f"\n  Reunion rate ratio (ours / ATSB): {ratio:.2f}x")

    # --- Plots ---
    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    fig.suptitle("ERA5 Drift: Per-Origin Analysis", fontsize=14)

    # 1. Reunion timed hit rate vs latitude
    ax = axes[0, 0]

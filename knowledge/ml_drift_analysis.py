def probability_map(results):
    """Generate probability map from Model A across the origin space."""
    clf = results["Model A: Reunion timing"]["clf"]

    print("\n" + "=" * 80)
    print("TASK 3: PROBABILITY MAP")
    print("=" * 80)

    # Grid: 0.5° spacing across the domain
    lat_range = np.arange(-42.0, -19.5, 0.5)
    lon_range = np.arange(82.0, 104.0, 0.5)

    grid_points = []
    for lat in lat_range:
        for lon in lon_range:
            grid_points.append((lat, lon))
    grid_points = np.array(grid_points)

    probs = clf.predict_proba(grid_points)[:, 1]

    # Save CSV
    csv_path = FIGURES_DIR / "reunion_probability_map.csv"
    with open(csv_path, "w") as f:
        f.write("lat,lon,p_reunion_timed\n")
        for (lat, lon), p in zip(grid_points, probs):
            f.write(f"{lat:.1f},{lon:.1f},{p:.6f}\n")
    print(f"\nSaved: {csv_path} ({len(grid_points)} grid points)")

    # ASCII heatmap — focus on the interesting region
    print("\n--- ASCII Probability Map (P(timed Reunion hit)) ---")
    print("Latitude range: 40S to 20S | Longitude range: 83E to 103E")
    print("Legend: . <0.5%  o 0.5-1%  O 1-2%  # 2-4%  @ 4-6%  * >6%\n")

    prob_grid = probs.reshape(len(lat_range), len(lon_range))

    # Print longitude header
    header = "      "
    for j, lon in enumerate(lon_range):
        if j % 4 == 0:
            header += f"{lon:>3.0f}"
        else:
            header += "   "
    print(header)

    for i, lat in enumerate(lat_range):
        row = f"{lat:>5.0f} "
        for j in range(len(lon_range)):
            p = prob_grid[i, j]
            if p >= 0.06:
                row += " * "
            elif p >= 0.04:
                row += " @ "
            elif p >= 0.02:
                row += " # "
            elif p >= 0.01:
                row += " O "
            elif p >= 0.005:
                row += " o "
            else:
                row += " . "
        print(row)

    print("\nPeak probability locations:")
    top_idx = np.argsort(probs)[::-1][:5]
    for idx in top_idx:
        lat, lon = grid_points[idx]
        print(f"  {lat:.1f}°, {lon:.1f}°E  P={probs[idx]:.1%}")

    # Plot
    fig, ax = plt.subplots(figsize=(14, 8))
    prob_2d = probs.reshape(len(lat_range), len(lon_range))
    im = ax.pcolormesh(lon_range, lat_range, prob_2d, cmap="YlOrRd", shading="auto")
    plt.colorbar(im, ax=ax, label="P(timed Reunion hit)")

    # Add contour lines
    cs = ax.contour(lon_range, lat_range, prob_2d, levels=[0.01, 0.02, 0.04, 0.06],
                     colors="black", linewidths=0.5, alpha=0.5)
    ax.clabel(cs, inline=True, fontsize=7, fmt="%.0f%%")

    # Mark zones
    from matplotlib.patches import Rectangle
    ax.add_patch(Rectangle((90, -37), 2, 3, fill=False, edgecolor="green", linewidth=2, linestyle="--", label="Our zone"))
    ax.add_patch(Rectangle((93, -40), 5, 20, fill=False, edgecolor="blue", linewidth=2, linestyle="--", label="ATSB corridor"))

    # Mark peak
    peak_idx = np.argmax(probs)
    peak_lat, peak_lon = grid_points[peak_idx]
    ax.plot(peak_lon, peak_lat, "w*", markersize=15, markeredgecolor="black", label=f"Peak: {peak_lat:.1f}°, {peak_lon:.1f}°E")

    ax.set_xlabel("Longitude (°E)")
    ax.set_ylabel("Latitude")
    ax.set_title("Reunion Timing Probability Map (GBClassifier on ERA5+OSCAR Drift)")
    ax.legend(loc="upper right")
    ax.grid(True, alpha=0.3)

    plt.tight_layout()
    fig.savefig(FIGURES_DIR / "reunion_probability_map.png", dpi=150)
    plt.close(fig)
    print(f"\nSaved: {FIGURES_DIR / 'reunion_probability_map.png'}")



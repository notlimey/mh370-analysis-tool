def honest_evaluation(origin_idx, timed_hit):
    """Print honest assessment of what this analysis can and cannot say."""
    n_per_origin = np.bincount(origin_idx).mean()
    reunion_rate = timed_hit[:, 0].mean()
    multi_rate = _multi_site_label(timed_hit).mean()

    print("\n" + "=" * 80)
    print("TASK 4: HONEST ASSESSMENT")
    print("=" * 80)

    print(f"""
Sample size: {int(n_per_origin):,} particles per origin, {len(origin_idx):,} total
  -> {int(n_per_origin):,} is large enough for stable rate estimates at the percent level.
  -> Statistical uncertainty on a 2% rate with 50,000 particles: ±0.12% (1σ binomial).
  -> The rate differences between origins (0% to 8%) are well above noise.

Class imbalance:
  Reunion timed hit rate: {reunion_rate:.2%} ({timed_hit[:, 0].sum():,} positives)
  Multi-site (2+ confirmed) rate: {multi_rate:.2%} ({_multi_site_label(timed_hit).sum():,} positives)
  -> Moderate imbalance but not extreme. AUC-ROC handles this well.

Model limitations:
  1. Only two features (lat, lon) — the classifier cannot learn trajectory shapes,
     only which starting positions are favored. This is by design: we want to know
     WHERE to look, not HOW debris gets there.
  2. Monthly mean ERA5 wind — misses synoptic variability (individual storms,
     cyclones). Monthly means capture the seasonal/spatial structure of the wind
     field but not weather events that could accelerate or deflect debris.
  3. OSCAR 5-day composites at 1/3° — misses mesoscale eddies (50-200 km).
     These eddies can trap or redirect debris. HYCOM at 1/12° daily would resolve them.
  4. Leeway coefficient fixed at 0.025 — varies with sea state, debris geometry,
     and windage area. The flaperon (high windage) likely has higher leeway than
     interior panels (low windage).
  5. Spherical advection on a 2D surface — ignores Stokes drift, wave effects,
     and vertical mixing in the upper ocean.
  6. The classifier interpolates/extrapolates from 100 discrete origin points.
     It cannot discover structure finer than the ~0.2° origin spacing.

What this analysis CAN say:
  - Which origin latitudes and longitudes along the 7th arc are oceanographically
    favored for producing timing-consistent Reunion arrivals
  - Whether latitude or longitude is the more discriminating feature
  - Whether the signal is consistent across multiple debris recovery sites
  - Whether the BTO/BFO-derived impact zone is in the favored region

What this analysis CANNOT say:
  - The precise crash location (the classifier has ~0.2° spatial resolution)
  - Whether the crash actually happened at the favored origin (correlation ≠ causation)
  - How results would change with daily wind or higher-resolution currents
  - Whether individual debris items followed the modeled trajectory statistics
""")



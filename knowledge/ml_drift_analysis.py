def load_data(npz_path: Path):
    """Load npz and attach origin lat/lon to each particle."""
    d = np.load(npz_path)
    origin_idx = d["origin_idx"]
    timed_hit = d["timed_hit"]
    any_hit = d["any_hit"]

    origin_lats = np.array([ORIGINS[i][0] for i in origin_idx])
    origin_lons = np.array([ORIGINS[i][1] for i in origin_idx])

    return origin_idx, origin_lats, origin_lons, timed_hit, any_hit



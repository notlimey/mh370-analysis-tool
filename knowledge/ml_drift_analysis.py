def main():
    parser = argparse.ArgumentParser(description="ML analysis of ERA5 drift dataset")
    parser.add_argument("--npz", type=Path,
                        default=Path(__file__).parent / "datasets" / "drift_era5_v2.npz")
    args = parser.parse_args()

    if not args.npz.exists():
        print(f"ERROR: {args.npz} not found", file=sys.stderr)
        print("Run the drift dataset generator first. See docs/research-note-era5-drift.md", file=sys.stderr)
        sys.exit(1)

    FIGURES_DIR.mkdir(parents=True, exist_ok=True)
    print(f"Loading {args.npz}...")

    origin_idx, origin_lats, origin_lons, timed_hit, any_hit = load_data(args.npz)
    print(f"Loaded {len(origin_idx):,} particles, {len(ORIGINS)} origins")

    # Task 1: Exploratory analysis
    df, rates, multi_score = exploratory_analysis(origin_idx, timed_hit)

    # Task 2: Classifiers
    results = train_classifiers(origin_lats, origin_lons, timed_hit)

    # Task 3: Probability map
